import pandas as pd
import numpy as np

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, confusion_matrix, classification_report

df = pd.read_csv('diabetes.csv')
df.head()

X = df[["Pregnancies","Glucose","BloodPressure","BMI","DiabetesPedigreeFunction","Age"]]
Y = df["Outcome"]

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.45, random_state=42)

model = LogisticRegression(max_iter=1000)
model.fit(X_train, Y_train)

y_pred = model.predict(X_test)

accuracy = accuracy_score(Y_test, y_pred)

print("Accuracy:", accuracy)

print(classification_report(Y_test, y_pred))

mse = np.mean((Y_test - y_pred) ** 2)
print("Mean Squared Error:", mse)

new_patient = [[int(input("enter the number of pregnencies: ")), int(input("enter the glucose level: ")), int(input("enter blood presure: ")), float(input("enter BMI: ")), float(input("enter DiabetesPedigreeFunction: ")), int(input("enter age: "))]]

prediction = model.predict(new_patient)

if prediction[0] == 1:
    print("The patient is likely to have Diabetes.")
else:
    print("The patient is not likely to have Diabetes.")